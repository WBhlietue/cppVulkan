    Mesh squareMesh;
    Mesh createSquareMesh()
    {
        Mesh mesh = {};
        mesh.vertices = {
            {{-1, -1}, {0.0f, 0.0f}},
            {{1, -1}, {1.0f, 0.0f}},
            {{1, 1}, {1.0f, 1.0f}},
            {{-1, 1}, {0.0f, 1.0f}}};
        mesh.indices = {0, 1, 2, 2, 3, 0};

        VkDeviceSize vBufferSize = mesh.vertices.size() * sizeof(Vertex);
        VkDeviceSize iBufferSize = mesh.indices.size() * sizeof(int);

        createBuffer(vBufferSize, VK_BUFFER_USAGE_VERTEX_BUFFER_BIT | VK_BUFFER_USAGE_TRANSFER_DST_BIT, VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT, mesh.vBuffer, mesh.vMemory);
        createBuffer(iBufferSize, VK_BUFFER_USAGE_INDEX_BUFFER_BIT | VK_BUFFER_USAGE_TRANSFER_DST_BIT, VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT, mesh.iBuffer, mesh.iMemory);
        uploadToDevice(mesh.vertices.data(), vBufferSize, mesh.vBuffer, mesh.vMemory);
        uploadToDevice(mesh.indices.data(), iBufferSize, mesh.iBuffer, mesh.iMemory);

        return mesh;
    }

    void DrawObject(VkCommandBuffer commandBuffer, VKObject object)
    {

        VkBuffer vertexBuffers[] = {object.mesh.vBuffer};
        VkDeviceSize offset[] = {0};
        vkCmdBindVertexBuffers(commandBuffer, 0, 1, vertexBuffers, offset);

        vkCmdBindIndexBuffer(commandBuffer, object.mesh.iBuffer, 0, VK_INDEX_TYPE_UINT32);

        vkCmdBindDescriptorSets(commandBuffer, VK_PIPELINE_BIND_POINT_GRAPHICS, graphicPipeline.pipelineLayout, 0, 1, &graphicPipeline.descriptorSet, 0, nullptr);
        ShaderConst sConst = {};
        sConst.id = object.id;
        sConst.screen_height = Height;
        sConst.screen_width = Width;
        sConst.color = object.material.color;
        sConst.pos = object.material.pos;
        sConst.size = object.material.size;
        sConst.scale = glm::vec2(1.0f, 1.0f);
        sConst.rotation = object.material.rotation;
        sConst.borderRadius = object.material.borderRadius;
        sConst.textureID = object.material.textureID;
        vkCmdPushConstants(commandBuffer, graphicPipeline.pipelineLayout, VK_SHADER_STAGE_VERTEX_BIT | VK_SHADER_STAGE_FRAGMENT_BIT, 0, sizeof(ShaderConst), &sConst);

        vkCmdDrawIndexed(commandBuffer, static_cast<uint32_t>(object.mesh.indices.size()), 1, 0, 0, 0);
    }

    void uploadToDevice(void *srcData, VkDeviceSize size, VkBuffer &dstBuffer, VkDeviceMemory &dstBufferMemory)
    {
        VkBuffer stagingBuffer;
        VkDeviceMemory stagingBufferMemory;
        createBuffer(size, VK_BUFFER_USAGE_TRANSFER_SRC_BIT,
                     VK_MEMORY_PROPERTY_HOST_VISIBLE_BIT | VK_MEMORY_PROPERTY_HOST_COHERENT_BIT,
                     stagingBuffer, stagingBufferMemory);
        void *data;
        vkMapMemory(device, stagingBufferMemory, 0, size, 0, &data);
        memcpy(data, srcData, (size_t)size);
        vkUnmapMemory(device, stagingBufferMemory);
        createBuffer(size,
                     VK_BUFFER_USAGE_TRANSFER_DST_BIT | VK_BUFFER_USAGE_VERTEX_BUFFER_BIT,
                     VK_MEMORY_PROPERTY_DEVICE_LOCAL_BIT,
                     dstBuffer, dstBufferMemory);
        copyBuffer(stagingBuffer, dstBuffer, size);
        vkDestroyBuffer(device, stagingBuffer, nullptr);
        vkFreeMemory(device, stagingBufferMemory, nullptr);
    }

    void initWindow()
    {
        glfwInit();

        glfwWindowHint(GLFW_CLIENT_API, GLFW_NO_API);

        window = glfwCreateWindow(Width, Height, "Vulkan", nullptr, nullptr);
        glfwSetWindowUserPointer(window, this);
        glfwSetFramebufferSizeCallback(window, framebufferResizeCallback);
        glfwSetMouseButtonCallback(window, mouseButtonCallback);
    }
    static void mouseButtonCallback(GLFWwindow *window, int button, int action, int mods)
    {
        auto app = reinterpret_cast<VulkanCore *>(glfwGetWindowUserPointer(window));
        if (button == GLFW_MOUSE_BUTTON_LEFT && action == GLFW_PRESS)
        {

            app->OnClick();
        }
    }

    void OnClick()
    {
        for (auto &obj : onClickCallbacks)
        {
            obj.Check(mousePositionX, mousePositionY);
        }
    }

    static void framebufferResizeCallback(GLFWwindow *window, int width, int height)
    {
        auto app = reinterpret_cast<VulkanCore *>(glfwGetWindowUserPointer(window));
        app->framebufferResized = true;
    }

    void initVulkan()
    {
        createInstance();
        createSurface();
        pickPhysicalDevice();
        createLogicalDevice();
        createSwapChain();
        createImageViews();
        createRenderPass();

        createCommandPool();
        createSyncObjects();
        textureManager.Init(device, physicalDevice, commandPool, graphicsQueue);
        LoadTextures();
        graphicPipeline.Create(device, renderPass, "shader/test_frag.spv", "shader/test_vert.spv", textureManager.textures);

        createFramebuffers();
        createCommandBuffers();
    }

    void mainLoop()
    {
        auto lastTime = std::chrono::high_resolution_clock::now();
        const float targetFrameTime = 1.0f / 60.0f; // 60 FPS
        while (!glfwWindowShouldClose(window))
        {
            auto currentTime = std::chrono::high_resolution_clock::now();
            std::chrono::duration<float> deltaTime = currentTime - lastTime;
            lastTime = currentTime;
            glfwPollEvents();
            glfwGetCursorPos(window, &mousePositionX, &mousePositionY);
            mousePositionX -= Width / 2;
            mousePositionY -= Height / 2;
            drawFrame();
            OnUpdate(deltaTime.count());
        }

        vkDeviceWaitIdle(device);
    }

    void cleanupSwapChain()
    {
        for (auto framebuffer : swapChainFramebuffers)
        {
            vkDestroyFramebuffer(device, framebuffer, nullptr);
        }

        for (auto imageView : swapChainImageViews)
        {
            vkDestroyImageView(device, imageView, nullptr);
        }

        vkDestroySwapchainKHR(device, swapChain, nullptr);
    }

    void cleanup()
    {
        cleanupSwapChain();

        graphicPipeline.Destroy(device);
        vkDestroyRenderPass(device, renderPass, nullptr);

        vkDestroyBuffer(device, indexBuffer, nullptr);
        vkFreeMemory(device, indexBufferMemory, nullptr);

        vkDestroyBuffer(device, vertexBuffer, nullptr);
        vkFreeMemory(device, vertexBufferMemory, nullptr);

        for (size_t i = 0; i < MAX_FRAMES_IN_FLIGHT; i++)
        {
            vkDestroySemaphore(device, renderFinishedSemaphores[i], nullptr);
            vkDestroySemaphore(device, imageAvailableSemaphores[i], nullptr);
            vkDestroyFence(device, inFlightFences[i], nullptr);
        }

        vkDestroyCommandPool(device, commandPool, nullptr);

        vkDestroyDevice(device, nullptr);

        vkDestroySurfaceKHR(instance, surface, nullptr);
        vkDestroyInstance(instance, nullptr);

        glfwDestroyWindow(window);

        glfwTerminate();
    }

    void recreateSwapChain()
    {
        int width = 0, height = 0;
        glfwGetFramebufferSize(window, &width, &height);
        while (width == 0 || height == 0)
        {
            glfwGetFramebufferSize(window, &width, &height);
            glfwWaitEvents();
        }

        vkDeviceWaitIdle(device);

        cleanupSwapChain();

        createSwapChain();
        createImageViews();
        createFramebuffers();
    }

